package com.SPA_4.demo.models;

public class BookingDTO {

    private String name;
    private int guests;

    public BookingDTO() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGuests() {
        return guests;
    }

    public void setGuests(int guests) {
        this.guests = guests;
    }

}
